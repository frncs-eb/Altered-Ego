package util;

/**
 * Represent the screens of the game.
 */
public enum GameScreen {
    /** The main screen. */
    TITLE,

    /** The mode selection screen. */
    SELECT_MODE,

    /** The character selection screen. */
    SELECT_CHARACTER,

    /** The battle screen. */
    BATTLE,

    /** The arcade screen */
    BATTLE_ARCADE,

    /** The result screen. */
    RESULT,

    /** The result for the arcade screen*/
    ARCADE_RESULT
}